/** Best-effort shop timezone from state; defaults to Eastern. */
export function shopTimezone(state: string | null | undefined): string {
  const key = state?.trim().toUpperCase();
  return (key && STATE_TZ[key]) || "America/New_York";
}

/** Shop-local datetime: "Jun 29, 2026 at 12:52 PM EDT". */
export function formatApprovalTimestamp(date: Date, timeZone: string): string {
  const parts = new Intl.DateTimeFormat("en-US", {
    month: "short",
    day: "numeric",
    year: "numeric",
    hour: "numeric",
    minute: "2-digit",
    hour12: true,
    timeZoneName: "short",
    timeZone,
  }).formatToParts(date);

  const get = (type: Intl.DateTimeFormatPartTypes) =>
    parts.find((p) => p.type === type)?.value ?? "";

  return `${get("month")} ${get("day")}, ${get("year")} at ${get("hour")}:${get("minute")} ${get("dayPeriod")} ${get("timeZoneName")}`.trim();
}
